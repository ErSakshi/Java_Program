class IPAdress
{
  public static void main(String []args)
   {
     short ipA=1, toA=126;
     short ipB=128, toB=191;
     short ipC=192, toC=223;
     short ipD=224, toD=239;
     short ipE=240, toE=254;

    System.out.println("Range of IP Address of A :"+ipA  +  " to "  +toA);
    System.out.println("Range of IP Address of B :"+ipB  +  " to "  +toB);
    System.out.println("Range of IP Address of C :"+ipC  +  " to "  +toC);
    System.out.println("Range of IP Address of D :"+ipD  +  " to "  +toD);
    System.out.println("Range of IP Address of E :"+ipE  +  " to "  +toE);
  }
}